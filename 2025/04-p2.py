import numpy as np

with open('2025/04.txt') as f:
    lines = [line.strip() for line in f]

matrix_list = [list(line) for line in lines]

matrix = np.array(matrix_list)
matrix[matrix == '.'] = 0
matrix[matrix == '@'] = 1

matrix = np.pad(matrix, pad_width=1, mode='constant', constant_values=0)

filter = [
    [1, 1, 1],
    [1, 0, 1],
    [1, 1, 1]
]

total_sum = 1
removed = 0

while total_sum > 0:

    total_sum = 0
    indices = []

    for i in range(1, matrix.shape[0] - 1):
        for j in range(1, matrix.shape[1] - 1):

            if matrix[i, j] == '1':
            
                local = np.array(matrix[i-1:i+2, j-1:j+2], dtype='int64')

                product = np.sum(local * filter)
                if product < 4:
                    total_sum +=1
                    indices.append([i, j])

    removed += len(indices)
    for index in indices:
        matrix[index[0], index[1]] = 0

print(removed)

    
